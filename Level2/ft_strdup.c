#include <stdlib.h>

char	*ft_strdup(char *src)
{
	char *dest;
	int i;
	int len;

	while (src[len])
		len++;
	dest = (char *)malloc(sizeof(char) * len + 1);
	i = 0;
	if (dest == NULL)
		return (NULL);
	while (src[i])
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}